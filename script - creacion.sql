		WHERE f.ID_EMPRESA = @idEmpresa
			AND f.ID_FACTURA not in (SELECT ID_FACTURA FROM POLACA_INVERSA.ITEMS_FACTURAS_RENDIDAS)
			AND (p.FECHA_PAGO >= d.FECHA_DEVOLUCION OR d.FECHA_DEVOLUCION IS NULL) 
