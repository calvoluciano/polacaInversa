				 JOIN [POLACA_INVERSA].PAGOS p on p.ID_PAGO = ip.ID_PAGO
				 LEFT JOIN [POLACA_INVERSA].ITEMS_DEVOLUCIONES i_d on  i_d.ID_FACTURA = f.ID_FACTURA
