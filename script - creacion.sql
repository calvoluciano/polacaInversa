															  @fechaHasta DATETIME)
RETURNS TABLE
AS
	RETURN (SELECT c.NOMBRE as 'Cliente', f.ID_FACTURA as 'Factura',f.TOTAL as 'Total',f.FECHA_VENCIMIENTO as 'Fecha Vencimiento'
				FROM [POLACA_INVERSA].FACTURAS f
